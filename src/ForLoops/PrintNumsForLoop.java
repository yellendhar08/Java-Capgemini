package ForLoops;

public class PrintNumsForLoop {
    public static void main(String args[]){

        //WAJP to print numbers in range using For Loop
        for(int i=1111; i<=1121; i++){
            System.out.print(i+" ");
        }

        System.out.println();
        for(int i=1234; i>=1223; i--){
            System.out.print(i+" ");
        }

        System.out.println();
        for(int i=-1234; i<=-1223; i++){
            System.out.print(i+" ");
        }
    }
}

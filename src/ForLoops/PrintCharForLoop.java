package ForLoops;

public class PrintCharForLoop {
    public static void main(String args[]){
        for(char i = 'd'; i<='j'; i++){
            System.out.print(i+" ");
        }

        System.out.println();
        for(char j ='m'; j>='e';j--){
            System.out.print(j+" ");
        }

        System.out.println();
        for(char k ='z'; k>='t'; k--){
            System.out.print(k+" ");
        }

        System.out.println();
        for(char m='r'; m<='z'; m++){
            System.out.print(m+" ");
        }
    }
}
